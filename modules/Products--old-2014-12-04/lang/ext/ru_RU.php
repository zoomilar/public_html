<p>Currently six types of custom fields are available: \'textbox\',\'checkbox\',and \'textarea, dropdown, and file\'.  Select one of these, give your new field a name, and indicate wether that field should be visible to the public in the various views.  Then hit submit</p>
<li>Create one or more products</li>
