<ul>
  <li>{products_getcategory categoryid=&#039;99&#039; [assign=&#039;tmp&#039;]}
  <p>Ce plugin permet la r&eacute;cup&eacute;ration de toutes les informations d&#039;une cat&eacute;gorie sp&eacute;cifique (s&eacute;lection par champ ID). Il peut de mani&egrave;re optionnelle assigner ces informations &agrave; une variable smarty (dans l&#039;exemple : <em>tmp</em>). Cette variable devient alors un objet contenant toutes les informations de la cat&eacute;gorie, y compris les valeurs des champs.<br/>Par exemple : <code>
