<p>Wenn Sie noch weitere Fotogalerien m&ouml;chten, erstellen Sie einfach ein Unterverzeichnis und laden Ihre Fotos wie beschrieben in dieses Verzeichnis. Wenn Sie dem {Gallery}-Tag weitere Parameter hinzuf&uuml;gen, k&ouml;nnen Sie ganz einfach steuern, welche untergeordnete Galerie mit welchem Template angezeigt werden soll, z.Bsp.{Gallery dir=&quot;Urlaub/Niederlande2009&quot; template=&quot;Lightbox&quot;}</p>
<p>Standardm&auml;&szlig;ig werden die von der Bildverwaltung erstellten Vorschaubilder verwendet, um die Fotogalerien anzuzeigen.</p>

<h3>Erweiterte Optionen (aber genau so einfach zu verwenden)</h3>
<p>In der Administration des Moduls finden Sie eine ganze Reihe weiterer Optionen:</p>
<ul>
<li>Legen Sie ein Foto als Voreinstellung f&uuml;r eine Galerie fest, welches dann in der &uuml;bergeordneten Galerie als &quot;Cover&quot; f&uuml;r diese Galerie angezeigt wird (anstatt des voreingestellten Verzeichnis-Icons).</li>
<li>F&uuml;gen Sie den Galerien Titel und Bescheibungen hinzu.</li>
<li>Legen Sie ein voreingestelltes Template f&uuml;r jede Galerie fest.</li>
<li>Legen Sie die Gr&ouml;&szlig;e der Vorschaubilder f&uuml;r jedes Template fest, mit den M&ouml;glichkeiten zum Skalieren, Beschneiden und Zoomen der Bilder.</li>
<li>Legen Sie f&uuml;r jedes Template fest, in welcher Reihenfolge die Fotos sortiert werden sollen.</li>
<li>&Auml;ndern Sie die Sortierung der Fotos manuell durch Drag and Drop in der Gallery-Liste. (Beachten Sie die &Auml;nderung des Mauszeigers)</li>
<li>F&uuml;gen Sie den Fotos Titel und Beschreibungen hinzu.</li>
<li>Deaktivieren Sie bestimmte Fotos oder Galerien, damit diese nicht angezeigt werden.</li>
<li>Bearbeiten/Kopieren Sie die Templates oder erstellen ein neues. &Uuml;ber das Info-Icon unterhalb des Template-Code erfahren Sie mehr &uuml;ber die verf&uuml;gbaren Variablen.</li>
</ul>
<p>Alle Titel, Beschreibungen und Einstellungen werden in der Datenbank gespeichert. Die Datenbank wird bei jedem Aufruf der Gallery-Administration mit den Verzeichnissen synchronisiert. Eine kleine Warnung: Wenn Sie ein Bild oder ein Unterverzeichnis umbenennen oder via Bildverwaltung, Dateiverwaltung oder FTP in ein anderes Verzeichnis verschieben, gehen dessen Titel, Beschreibungen und Einstellungen verloren. Verwenden Sie daher IMMER die entsprechende Funktion des Gallery-Moduls.</p>

<h3>Hochladen und Gr&ouml;&szlig;en&auml;nderung von Bildern</h3>
<p>Das Gallery-Modul hat eine eigene Funktion zum Hochladen von Bildern. F&uuml;r den Auswahl-Dialog via Ctrl/Shift zum Hochladen mehrerer Bilder wird das SWFUpload JavaScript und die Flash Bibliothek verwendet. Damit dies funktioniert, muss f&uuml;r Ihren Browser ein aktuelles Flash Player plugin installiert sein.</p>
<p>Um das Hochladen sehr gro&szlig;er Bilder zu verhindern, k&ouml;nnen Sie in der Registerkarte &quot;Optionen&quot; eine maximale Gr&ouml;&szlig;e festlegen, die hochgeladen werden darf. Gr&ouml;&szlig;ere Bilder werden vor dem Hochladen entsprechend Ihren Vorgaben skaliert. Die Proportionen der Bilder bleiben dabei unver&auml;ndert.</p>

<h3>Benutzerdefinierte Felder</h3>
<p>F&uuml;r das Gallery-Modul lassen sich verschiedene, benutzerdefinierte Felder festlegen. Ein benutzerdefiniertes Feld kann auch f&uuml;r eine untergeordnete Galerie oder ein einzelnes Bild festgelegt werden.</p>
<p>Benutzerdefinierte Felder k&ouml;nnen im Gallery-Template direkt mit <code>{$fields.ihr_feld_alias.name}</code> und <code>{$fields.ihr_feld_alias.value}</code> aufgerrufen werden. Sie k&ouml;nnen auch eine foreach-Schleife auf <code>{$fields}</code> erstellen.</p>
<p>Benutzerdefinierte Felder f&uuml;r einzelne Bilder oder untergeordnete Galerien k&ouml;nnen innerhalb der $images-foreach-Schleife im Gallery-Template mit <code>{$image->fields.ihr_feld_alias.name}</code> and <code>{$image->fields.ihr_feld_alias.value}</code> aufgerufen werden.</p

<h3>Bearbeiten, Im- und Exportieren von Gallery-Templates</h3>
<p>Um Templates bearbeiten zu k&ouml;nnen, m&uuml;ssen die entsprechenden Benutzer Mitglieder einer Gruppe mit der Berechtigung &#039;Modify Templates&#039; sein.</p>
<p>Gallery-Templates k&ouml;nnen separat von der <a href="http://dev.cmsmadesimple.org/project/files/726" target="_blank">Gallery-Modul-Projektseite</a> heruntergeladen werden. Wenn der Name des Templates bereits vorhanden ist und das entsprechende Kontrollfeld aktiviert ist, wird dieses beim Import &uuml;berschrieben. Bitte beachten Sie, dass eine Aktualisierung des Gallery-Moduls keinerlei Auswirkungen auf Ihre Modul-Templates hat.</p>
<p>Wenn Sie eigene Templates erstellen und diese exportieren wollen, achten Sie darauf, dass sich alle ben&ouml;tigten Dateien (genau wie die voreingestellten Templates) in einem eigenen Verzeichnis mit dem Template-Namen im Verzeichnis modules/Gallery/templates/ befinden. Diese Dateien werden dann automatisch der XML-Datei hinzugef&uuml;gt. Klicken Sie auf den Link &#039;&Uuml;ber&#039;, um die Versionsnummer und den Infotext zu bearbeiten.</p>
<p>Im Template-Listenfeldmen&uuml; k&ouml;nnen Templates als unsichtbar definiert werden, so dass diese Templates von normalen Administratoren nicht ausgew&auml;hlt werden k&ouml;nnen, sondern nur von Benutzern, die die Berechtigung &#039;Modify Templates&#039; haben.</p>

<h3>Globale Gallery-Optionen</h3>
