$lang['helptext'] = '<h3>What Does This Do?</h3>
<p>This module lets you create online catalogs or portfolios. Catalogs consist of &quot;Catalog Items&quot; which could be products, works of art, or the like, and &quot;Catalog Categories&quot; which could be item categories or other natural divisions of the catalog.</p>
<p>Categories may be defined hierarchically (using the standard CMS Made Simple content list). Category pages can display items in the category and/or subcategories, optionally recursing through sub-categories. This behavior can be configured on a per-category basis.</P>
<p>There is also a Printable Catalog, which represents the whole collection of Catalog Items on a single page (this is currently implemented badly, and will load slowly).</p>
<p>The module has built-in support for &quot;Content Aliases&quot; (a module available at <a href="http://dev.cmsmadesimple.org/projects/contentaliases/">the Developer Forge</a>), which allows you to place any Catalog Item into multiple Catalog Categories.</p>
<h3>How Do I Use It</h3>
<p>When you install this module, it creates three new Content Types: Catalog Item, Catalog Category, and Printable Catalog. When you&#039;re in your site administration category, you add Catalog Items and Catalog Categories just as you would any other kind of page. Select Content &amp;gt; Pages &amp;gt; Add Content, and then select &quot;Catalog Item&quot; or &quot;Catalog Category&quot; from the Content Type pulldown.</p>
<h4>Catalog Items</h4>
<p>Adding a Catalog Item is similar to adding an ordinary page to your site. The data fields for the Item are not exactly the same, however. Also note that in the &quot;Images&quot; tab, you can select multiple images to upload for the item. When you upload images, the system will size them appropriately for the catalog, create thumbnails for use in the Item&#039;s page, create thumbnails for any Category pages, and so on. This requires that you have GD or a similar image library installed. Currently, only jpeg format images are supported.</p>
<h4>Catalog Categories</h4>
<p>A Catalog Category is used for organizing your catalog items. It provides a page that lists Cataloger content that is contained by it. A Catalog Item is considered to be part of a Catalog Category if it is below that Category in the Site Hierarchy. Categories can similarly include other Categories.</p>
<p>Catalog Categories have a number of settings to determine how they should display the Items and Categories they contain: if you look at the &quot;Options&quot; tab, you can choose how many items and/or categories to show, what order to show them in, whether to display only items or only categories or both, and how many levels of the hierarchy to display below the category page.</p>
<p>For example, consider the following structure:</p>
<ol>
	<li><b>Hats</b>
		<ol>
			<li><b>Fashion</b>
			 	<ol>
			 		<li>Feathered Hat</li><li>Fedora </li><li>Baseball Hat</li>
			 	</ol>
			 </li>
		 	<li><b>Work Hats</b>
		    	<ol>
		    		<li>Hard Hat</li><li>Diving Helmet</li>
		    	</ol>
		    </li>
		</ol>
	</li>
</ol>
<p>In this diagram, bolded items are Catalog Categories, and non-bolded items are Catalog Items. Depending upon how you set your options, the Hats Category Page can show only entries for the Fashion and Work Hats Category Pages, or it can show entries for Fashion, Work Hats, and all the hats listed below them.</p>
<p>You can opt to show the items in &quot;natural&quot; order (i.e., the order the Items show up in the CMS Content Hierarchy), or in alphabetical order.</P>
<p>If you have large numbers of items in a Category, you can set the maximum number to be displayed in a Category Page, and the page will automatically create links to navigate through the list.</p>
<p>Each Category page can have different settings for sort order, levels to display, and number of items. You can set the default for these values in Extensions &amp;gt; Cataloger &amp;gt; Manage Preferences. If, at a later date, you wish to change some setting on all of your Category pages, you can go into Extensions &amp;gt; Cataloger &amp;gt; Global Catalog Options, and make those changes.</p>
<h4>Images</h4>
<p>Cataloger allows you to select different size images for Item pages, Category pages, and the Printable Catalog. You can set these defaults in Extensions &amp;gt; Cataloger &amp;gt; Manage Preferences.</p>
<p>When you upload images, the original is stored. When someone visits a page, the reduced size images are requested using a special URL, which will redirect the user&#039;s browser to the scaled image if it exists, and creating the scaled image if it doesn&#039;t. This allows you to change the size of images, without having to re-upload all the images, or rescaling them all at once.</p>
<p>This image rescaling code requires that you have either ImageMagick or GD lib installed, and configured in your CMS Made Simple config.php.</p>
<h4>Files</h4>
<p>Cataloger allows you to associate arbitrary files with any Item page. These are uploaded via the admin, just like images. There
have been rudimentary security considerations (e.g., file-extension-based restrictions), however, as with any file upload
capability that puts files inside the web root, if you do not trust your uploaders, you are at some degree of risk. You have been warned.</p>
<h3>Feature Lists</h3>
<p>You can, as of version 0.4, have &quot;feature lists&quot; which are the <i>n</i> most-recently added catalog items, or a collection of <i>k</i> random items from the catalog.</p>
<h4>&quot;All&quot; Feature List</h4>
<p>To get a list of all catalog items (optionally under a specified part of your hierarchy) for processing via a template, you can use
the &quot;all&quot; feature list.</p>
<p>The syntax for an &quot;all&quot; list is like:</p>
<p>&amp;#123;Cataloger action=&#039;all&#039; sub_template=&#039;my_sub_template&#039;}, where sub_template is the template to use to render the list.
There is an optional parameter, alias=&#039;page_alias&#039;, where page_alias indicates the top of the tree (e.g., a place in your menu
hierarchy, typically a category page) in which to look for new items. A special value for &quot;page_alias&quot; is &quot;/&quot;, which means to
start at the root.</p>
<p>This is useful for catalog-wide operations. For example, to get a list of all categories (and do something with them),
you could use the tag &amp;#123;Cataloger action=&#039;all&#039; sub_template=&#039;my_sub_template&#039; alias=&#039;/&#039; recurse=&#039;categories_all&#039;}. Your template could then throw
those categories into a pulldown menu that redirected you to that category&#039;s page, for example:</p>
<pre>
&amp;lt;select name=&quot;foo&quot; onchange=&quot;document.location=this.options[this.selectedIndex].value;&quot;>
&amp;lt;option value=&quot;&quot;>Select A target&amp;lt;/option>
&amp;#123;foreach from=$items item=thisone}
   &amp;lt;option value=&quot;&amp;#123;$thisone.link}&quot;>&amp;#123;$thisone.title}&amp;lt;/option>
&amp;#123;/foreach}
&amp;lt;/select>
</pre>
<p>If you wanted that to only show categories that were in the very top level of your hierarchy, you&#039;d replace the recurse=&#039;categories_all&#039; with
recurse=&#039;categories_one&#039;. Or if you wanted it to be all catalog Items instead of categories, you could use a tag like:
&amp;#123;Cataloger action=&#039;all&#039; sub_template=&#039;my_sub_template&#039; alias=&#039;/&#039; recurse=&#039;items_all&#039;}
<p>Similarly, you could have it list only categories under the current page (by omitting the &quot;alias=&#039;/&#039;&quot;). The combinations are basically limitless!</p>
<p>All supported values of recurse include:<ul>
<li>items_one - Catalog items, no more than one level below the current page (or alias point)</li>
<li>items_all - Catalog items, at any level below the current page (or alias point)</li>
<li>categories_one - Categories, no more than one level below the current page (or alias point)</li>
<li>categories_all - Categories, at any level below the current page (or alias point)</li>
<li>mixed_one - Catalog items and Categories, no more than one level below the current page (or alias point)</li>
<li>mixed_all - Catalog items and Categories, at any level below the current page (or alias point)</li>
</ul>
</p>
<p>Items in these lists come back in hierarchy order. You can change this using the &#039;global_sort&#039; parameter. Your choices are:</p>
<ul>
<li>alpha - alphabetically by title</li>
<li>date - in order of item creation date</li>
<li>mdate - in order of item modification date</li>
</ul>
<p>You may order these sorts using the &#039;global_sort_dir&#039; parameter, which will allow &#039;asc&#039; or &#039;desc&#039;.</p>
<p>So, for a thorough example:</p>
<p>&amp;#123;Cataloger action=&#039;all&#039; sub_template=&#039;my_sub_template&#039; alias=&#039;/&#039; recurse=&#039;categories_all&#039; global_sort=&#039;alpha&#039; global_sort_dir=&#039;asc&#039;}</p>
<h4>Recently Added Feature List</h4>
<p>To use a &quot;most-recently added&quot; list will allow you to display the most recently-added catalog items under a specified part of your hierarchy.</p>
<p>The syntax for a &quot;most recently added&quot; list is like:</p>
<p>&amp;#123;Cataloger action=&#039;recent&#039; sub_template=&#039;my_sub_template&#039;}, where sub_template is the template to use to render the list.
There are four optional parameters, count=&#039;3&#039; alias=&#039;page_alias&#039;, global_sort=&#039;date&#039;, global_sort_dir=&#039;desc&#039; where count
is the number of items to include, page_alias indicates the top of the tree (e.g., a place in your menu hierarchy,
typically a category page) in which to look for new items. A special value for &quot;page_alias&quot; is &quot;/&quot;, which means
to use <i>all</i> pages in the site. global_sort may be &#039;date&#039; (for item creation date) or &#039;mdate&#039; for modification date, and
global_sort_dir may be &#039;asc&#039; or &#039;desc&#039;.</p>
<h4>Random Items Feature List</h4>
<p>The syntax for a &quot;random&quot; list is like:</p>
<p>&amp;#123;cCataloger action=&#039;random&#039; sub_template=&#039;my_sub_template&#039;}, where sub_template is the template to use to render the list. There are two optional parameters, count=&#039;3&#039; alias=&#039;page_alias&#039;, where count is the number of items to include, and page_alias indicates the top of the tree (e.g., a place in your menu hierarchy, typically a category page) in which to look for new items. A special value for &quot;page_alias&quot; is &quot;/&quot;, which means to use <i>all</i> pages in the site.</p>
<h4>Variable</h4>
<p>You can grab variables (attributes) outside of your Cataloger templates, say in a global content block, using the variable method. The syntax looks like:</p>
<p>&amp;#123;Cataloger action=&#039;variable&#039; name=&#039;itemnotes&#039; default=&#039;no notes&#039;}, where name is the attribute to display (using the same all lower-case, punctuation-free representation like the smarty template variables), and default is the value to use if the attribute is not defined.</p>
<h4>Item Comparisons</h4>
<p>As of version 0.7.7, there is a product comparison action:</p>
<p>&amp;#123;cCataloger action=&#039;compare&#039; sub_template=&#039;my_sub_template&#039; items=&#039;item1,item2,item3&#039;}, where sub_template is the template to use to render the list, and items is a comma-delimited list of item page aliases to compare. Bolder users may also choose to pass in the items to compare as request variables, thereby enabling dynamic comparisons based on user selections. Do this by setting the &quot;items&quot; variable on the request passed to the page containing the Cataloger tag with a compare action.</p>
