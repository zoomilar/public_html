to use <i>all</i> pages in the site. global_sort may be &#039;date&#039; (for item creation date) or &#039;mdate&#039; for modification date, and
global_sort_dir may be &#039;asc&#039; or &#039;desc&#039;.</p>
<h4>Random Items Feature List</h4>
<p>The syntax for a &quot;random&quot; list is like:</p>
<p>{cCataloger action=&#039;random&#039; sub_template=&#039;my_sub_template&#039;}, where sub_template is the template to use to render the list. There are two optional parameters, count=&#039;3&#039; alias=&#039;page_alias&#039;, where count is the number of items to include, and page_alias indicates the top of the tree (e.g., a place in your menu hierarchy, typically a category page) in which to look for new items. A special value for &quot;page_alias&quot; is &quot;/&quot;, which means to use <i>all</i> pages in the site.</p>
<h4>Variable</h4>
<p>You can grab variables (attributes) outside of your Cataloger templates, say in a global content block, using the variable method. The syntax looks like:</p>
<p>{Cataloger action=&#039;variable&#039; name=&#039;itemnotes&#039; default=&#039;no notes&#039;}, where name is the attribute to display (using the same all lower-case, punctuation-free representation like the smarty template variables), and default is the value to use if the attribute is not defined.</p>
<h4>Item Comparisons</h4>
<p>As of version 0.7.7, there is a product comparison action:</p>
<p>{cCataloger action=&#039;compare&#039; sub_template=&#039;my_sub_template&#039; items=&#039;item1,item2,item3&#039;}, where sub_template is the template to use to render the list, and items is a comma-delimited list of item page aliases to compare. Bolder users may also choose to pass in the items to compare as request variables, thereby enabling dynamic comparisons based on user selections. Do this by setting the &quot;items&quot; variable on the request passed to the page containing the Cataloger tag with a compare action.</p>
