<p>The syntax for a &quot;most recently added&quot; list is like:</p>
<p>&amp;#123;cms_module module=&#039;Cataloger&#039; action=&#039;recent&#039; sub_template=&#039;my_sub_template&#039;}, where sub_template is the template to use to render the list. There are two optional parameters, count=&#039;3&#039; alias=&#039;page_alias&#039;, where count is the number of items to include, and page_alias indicates the top of the tree (e.g., a place in your menu hierarchy, typically a category page) in which to look for new items. A special value for &quot;page_alias&quot; is &quot;/&quot;, which means to use <i>all</i> pages in the site.</p>
