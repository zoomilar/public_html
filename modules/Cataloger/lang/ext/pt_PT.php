<h3>Customization and Advanced Topics</h3>
<h4>Catalog Item Attributes</h4>
<p>The default item attributes are typical for a catalog of products or artworks, but by going into Extensions &amp;gt; Cataloger &amp;gt; Manage User-Defined Attributes, you can change the attributes. It&#039;s best to define the attributes before you start entering Catalog Items.</p>
<p>Note that the attributes you define may need to be added to the template that you&#039;re using to display Catalog Items -- see Custom Templates below.</p>
<h4>Catalog Category Attributes</h4>
<p>Similarly, you can set attributes for Categories. Again, the attributes you define may need to be added to the template that you&#039;re using to display Catalog Categories -- see Custom Templates below.</p>
<h4>Printable Catalog Attributes</h4>
<p>Likewise, you can set attributes for Printable Catalogs. Again, the attributes you define may need to be added to the template that you&#039;re using to display your Printable Catalog -- see Custom Templates below.</p>
<h4>Custom Templates</h4>
<p>If you&#039;re willing to mess around with Smarty Templates, you can change the layout of any of the pages generated by Cataloger. This should make certain developers very happy, even if the inefficiency of the rest of the code will not. There are four kinds of templates: one kind for Catalog Items, one kind for Catalog Categories, and one kind for Printable Catalogs, and one kind for &quot;Feature&quot; lists, such as recently added items or random selections.</p>
<p>When editing a Template, the admin screen will display a list of Smarty tags available to you for that kind of template. This
will only happen once the module knows what kind of template you&#039;re editing, so when you first create the template, it displays all
the Smarty tags it knows about, only some of which being applicable.</p>
<h4>Smarty tags in Attributes</h4>
<p>Sometimes, you&#039;ll want the descriptions of your items to include smarty tags (e.g., cms_selflink). To make that work, you&#039;ll need to
update your template. For the attributes where you want smarty tags to be active, you&#039;ll need to nest the smarty evaluations in your template. For example:</p>
<pre>
&amp;#123;eval var=$attrlist[at].key}
</pre>
<p>becomes:</p>
<pre>
&amp;#123;eval var=$entry->input assign=thisAttr}&amp;#123;eval var=$thisAttr}
</pre>
<p>Ugly? Yup. But it works.</p>
<h3>Cataloger Image System</h3>
<p>Cataloger has an image system that will allow you to dynamically change the image display size. If you do not like it, you can alter
the templates and use any image system you want.</p>
<p>When you upload an image using Cataloger, it gets put in a special set of directories to make the imaging easier. You will see more
about this later.</p>
<p>It works by calling a PHP program instead of directly referencing the image. The program uses the filename to see if the
image already exists in the correct size. If so, it redirects to that image. Otherwise, it uses your GDLIB or ImageMagick to create
the image in the correct size, and then redirects to it.
</p>
<h4>Cataloger Image Filespec Explanation</h4>
<p>A cataloger image has a filespec like:
<br />
root_path/modules/Cataloger/Cataloger.Image.php?i=itemname_f_1_400_1.jpg&amp;amp;ac=82888
</p>
<p>The interesting thing is &quot;itemname_f_1_400_1.jpg&quot; - the stuff before is just a call to the program, and the &amp;amp;ac=82888 is a random thing that
is designed to prevent browser caching. So examining the name piece by piece (separating at the underscores), we have:</p>
<ul>
<li>item name - this is the alias of the item/category page containing the image.</li>
<li>image type - this is a designation for image type:<ul>
<li>f - item full-size image</li>
<li>t - item thumbnail</li>
<li>cf - category full-sized image</li>
<li>ct - category thumbnail</li>
<li>ctf - printable catalog full-sized image</li>
</ul>
</li>
<li>image number - so a given item/catalog/etc can have multiple images</li>
<li>long dimension - long dimension of the image, in pixels</li>
<li>missing flag - 1 if you want to show an &quot;image missing&quot; image if this image doesn&#039;t exist, 0 otherwise</li>
</ul>
<p>
This will take an image in root_path/uploads/images/catalog_src/itemname_src_1
</p>
